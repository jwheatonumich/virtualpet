function createPixels() {

    //Select the parent div
    var parentDiv = document.getElementById("parentDiv");
    parentDiv.style.width= "160px"; //16 pixels per row

    //Create pixel divs
    var pixelDiv = document.createElement("div");
    pixelDiv.className = "pixelDiv";
    pixelDiv.style.backgroundColor= "black";
    pixelDiv.style.width= "10px";
    pixelDiv.style.height= "10px";
    pixelDiv.style.display = "inline-block" //Display side-by-side

    parentDiv.appendChild(pixelDiv)

    pixelArray.push(0)
}

//Create image based on a pixel array
function displayPixels(inputArray) {
    let parentDiv = document.getElementById("parentDiv");
    let pixelList = parentDiv.querySelectorAll('.pixelDiv') //For some reason has extra entries at end
    for (let i=0; i<256; i++){ //Only works for exactly 256 pixels. 
        
        if(inputArray[i] == 1){
            pixelList[i].style.backgroundColor="black"
        }else{
            pixelList[i].style.backgroundColor="white"
        }
    }

    return inputArray
}

//Update image based on a pixel array
function updatePixels(inputArray){
    let parentDiv = document.getElementById("parentDiv");
    let pixelList = parentDiv.querySelectorAll('.pixelDiv') //For some reason has extra entries at end
    for (let i=0; i<256; i++){ //Only works for exactly 256 pixels. 
        
        if(inputArray[i] == 1){
            pixelList[i].style.backgroundColor="black"
        }
    }
}

